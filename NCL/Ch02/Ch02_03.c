/* 檔案名稱： Ch02_03*/
/* 完成日期： 20210508*/
#include <Stdio.h>

int main(void)
{
  printf("char   型別的長度: %d\n", sizeof(char));
  printf("int    型別的長度: %d\n", sizeof(int));
  printf("float  型別的長度: %d\n", sizeof(float));
  printf("double 型別的長度: %d\n", sizeof(double));
	
  return 0;
}